    private void validateFormat() {
        String nik = txtNIK.getText();
        String password = txtPassword.getText();
        if (!nik.isEmpty() && !password.isEmpty()) {
            //jika ga kosong (ada isinya)
            if (nik.matches("[0-9]+")){
                if (nik.length()>7){
                
                    new LoginController(nik,password).validateUser();
                }else{DialogView.showMessage(this, "NIK harus 8 angka.", DialogView.ERROR_MESSAGE);
       }
            }else{
                DialogView.showMessage(this, "NIK harus berisi angka.", DialogView.ERROR_MESSAGE);
            }
        }else{
        DialogView.showMessage(this, "NIK atau Password masih kosong.", DialogView.ERROR_MESSAGE);
        }
    }
